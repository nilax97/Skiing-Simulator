public class area {
    int id;
    int x, y;
    float min, max;
    float value;
    float step;

    public area(int id, int x, int y, float min, float max, float value, float step) {
        this.id = id;
        this.x = x;
        this.y = y;
        this.min = min;
        this.max = max;
        this.value = value;
        this.step = step;
    }
}
